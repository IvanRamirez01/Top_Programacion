
package calculadora;

public class Calculadora {
    
    public double resultado;
    
    public void calcsuma(double n1, double n2){
        resultado = n1 + n2;
    }
    public void calcresta(double n1, double n2){
        resultado = n1 - n2;
    }
    public void calcmult(double n1, double n2){
        resultado = n1 * n2;
    }
    public void calcdiv(double n1, double n2){
        resultado = n1 / n2;
    }
    
}
